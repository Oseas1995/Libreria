    public void obtenerTotal(){
        float fila = 0;
        float total = 0;
        
        for (int i=0 ; i < tblCompras.getRowCount() ; i++){
            fila = Float.parseFloat(tblCompras.getValueAt(i, 4).toString());
            total += fila;
        }
        lblTotal.setText("El total es: " + total);
                
    }
    
    public void traerProveedor() {
        cboNombreProveedor.addItem("Seleccione...");
        prov = proveedorDB.ListProveedor();
        for (Proveedor to : prov) {
            cboNombreProveedor.addItem(to.getNombre());
        }
    }
    
    public void LimpiarFormulario(){
        DefaultTableModel tb=(DefaultTableModel)tblCompras.getModel();
        for(int i = tb.getRowCount()-1;i >= 0; i--){
            tb.removeRow(i);
        }
    }
    
