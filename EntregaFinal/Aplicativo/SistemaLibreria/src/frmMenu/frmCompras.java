    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        Compra c = new Compra();
        c.setIdLibro(Integer.parseInt(txtIdLibro.getText()));
        c.setIdProveedor(idProveedor);
        c.setCantidad(Integer.parseInt(txtCantidad.getText()));
        c.setNombreLibro(txtNombreLibro.getText());
        c.setPrecioCosto(Float.parseFloat(txtPrecioCosto.getText()));
        db.insertarCompra(c);
        
        DefaultTableModel tb=(DefaultTableModel)tblCompras.getModel();
        tb.addRow(new Object[]{tblCompras.getRowCount()+1,c.getNombreLibro(),c.getCantidad(),c.getPrecioCosto(),c.getCantidad()*c.getPrecioCosto()});
        
    }//GEN-LAST:event_btnComprarActionPerformed

