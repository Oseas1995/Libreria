    private void tblEmpleadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmpleadoMousePressed
        JTable table = (JTable) evt.getSource();
        Point point = evt.getPoint();
        int row = table.rowAtPoint(point);
        if(!(row == -1)){
            txtPNombre.setText((String) tblEmpleado.getValueAt(row, 0));
            txtSNombre.setText((String) tblEmpleado.getValueAt(row, 1));
            txtPApellido.setText((String) tblEmpleado.getValueAt(row, 2));
            txtSApellido.setText((String) tblEmpleado.getValueAt(row, 3));
            txtDireccion.setText((String) tblEmpleado.getValueAt(row, 4));
            txtCorreo.setText((String) tblEmpleado.getValueAt(row, 5));
            txtNoIdentidad.setText((String) tblEmpleado.getValueAt(row, 6));
        }
    }//GEN-LAST:event_tblEmpleadoMousePressed

