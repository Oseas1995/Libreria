    public void ListarDatos(){
        empleado=db.ListEmpleado();
        DefaultTableModel tb=(DefaultTableModel)tblEmpleado.getModel();
        for(Empleado el:empleado){
            tb.addRow(new Object[]{el.getpNombre(),el.getsNombre(),el.getpApellido(),el.getsApellido(),el.getDireccion(),el.getCorreo(),el.getNoIdentidad()});
        }
    }
    
    public void LimpiarFormulario(){
        DefaultTableModel tb=(DefaultTableModel)tblEmpleado.getModel();
        for(int i = tb.getRowCount()-1;i >= 0; i--){
            tb.removeRow(i);
        }
    }
